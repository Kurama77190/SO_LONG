#include "so_long.h"

void	ft_init_game(t_game *data)
{
	data->memory_manager = NULL;
	data->anim_actived = -1;
	// data->pos_char_x = 0;
	// data->pos_char_y = 0;
	data->mlx_ptr = mlx_init();
	init_animations(data);
	init_map(data);

	return ;
}

