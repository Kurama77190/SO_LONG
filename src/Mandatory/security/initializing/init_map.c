#include "so_long.h"

void	draw_map(t_game *data);

void	init_map(t_game *data)
{
	data->map = read_map(data, data->n_map, &data->map_width, &data->map_height);
	draw_map(data);
	if (!data->buffer)
	{
		data->buffer = ft_calloc(1, sizeof(t_img), data->memory_manager, "MLX");
		data->buffer->img_ptr = mlx_new_image(data->mlx_ptr, data->map_width * 64, data->map_height * 64);
		data->buffer->addr = mlx_get_data_addr(data->buffer->img_ptr, &data->buffer->bpp, &data->buffer->line_length, &data->buffer->endian);	
	}
}

