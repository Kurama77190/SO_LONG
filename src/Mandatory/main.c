#include "so_long.h"
#include <mlx.h>

int	main(int argc, char **argv, char **env)
{
	t_game	data;

	memset(&data, 0, sizeof(data));
	if (argc != 2)
	{
		ft_putstr_fd("Bad arguments.\n", 2);
		return (1);
	}
	if (!env[0])
	{
		ft_putstr_fd("environment doesn't exist.\n", 2);
		return (1);
	}
	data.n_map = argv[1];
	ft_init_game(&data);
	mlx_hook(data.win_ptr, 2, 1L << 0, keypress_hook, &data);
	mlx_hook(data.win_ptr, 3, 1L << 1, keyrelease_hook, &data);
	mlx_hook(data.win_ptr, 17, 0, close_window, &data);
	mlx_loop_hook(data.mlx_ptr, update_game, &data);
	mlx_loop(data.mlx_ptr);
	return (0);
}

